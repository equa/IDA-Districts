    def TreeItem_Add(self, level, mdlIdx,dlg):
        """Function to add new project version and add child item to treeview"""
        versionName=dlg.input.text()
        description=dlg.input_description.text()
        #print('****************add version*****************')
        if versionName:
            newVersionName=self.checkIfVersionIsNew(versionName)
            if newVersionName:
                if checkString(versionName):
                    
                    #Creating version --> new schema in project        
                    idBase=0
                    item = self.model.itemFromIndex(mdlIdx)
                    if item:
                        baseName=item.text()
                        sql="SELECT id FROM public.versionhandling WHERE name = '"+baseName+"';"
                        #print(sql)
                        self.cur.execute(sql)
                        for base in self.cur.fetchall():
                            idBase = base['id']         
                    sql = 'INSERT INTO public.versionhandling (name,id_base,description) VALUES (\''+versionName+'\','+str(idBase)+',\''+description+'\');'
                    #print(sql)
                    self.cur.execute(sql)

                    temp_key = QStandardItem(versionName)
                    temp_value1 = QStandardItem('')
                    self.model.itemFromIndex(mdlIdx).appendRow([temp_key, temp_value1])
                    copy_schema(item.text(),versionName,self.dictDB,self.cur,self.plugin_dir)
                    
                    #check if supervisory control exists
                    if os.path.exists(getModellingDir(self.plugin_dir)+'\\models\\{}\\{}\\supervisory_control\\supervisory_control.idm'.format(self.dictDB['projectName'],item.text())):
                        copy_tree_filter_extensions_and_folders(getModellingDir(self.plugin_dir)+'\\models\\{}\\{}\\supervisory_control\\'.format(self.dictDB['projectName'],item.text()),getModellingDir(self.plugin_dir)+'\\models\\{}\\{}\\supervisory_control\\'.format(self.dictDB['projectName'],versionName))
                    
                    data=self.getVersionData()
                    self.importData(data)
                    self.dlg.treeViewVersions.expandAll()   
                    self.dictDB['versionName']=versionName
                    writeDBSettings(self.plugin_dir,self.dictDB)
                    self.loadVersion()
                    item=self.get_item_by_text(versionName)
                    highlight_item(item,self.model.invisibleRootItem())
                    self.dlg.treeViewVersions.viewport().update()
                    closeDialog(dlg)
            else:
                self.iface.messageBar().pushMessage("Info", "Base version {} does already exist!".format(versionName), level=Qgis.Info)
        else:
            self.iface.messageBar().pushMessage("Info", "Please enter a version name!", level=Qgis.Info)

