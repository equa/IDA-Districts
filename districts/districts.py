    def saveDistrictsSettings(self,dlg):
        """Save the districts settings to QSettings"""
        settings = QSettings()        
        settings.beginGroup("districts")
        pathProjects=standardizePath(dlg.lineEdit_pathProjects.text(),create_if_not_exists=False)
        settings.setValue("pathProjects",pathProjects)
        if not os.path.exists(pathProjects):
            dlg_question = QMessageBox(dlg)
            dlg_question.setWindowTitle('IDA Project Path does not exist!')
            dlg_question.setText("""Do you want to create the project folder?""")
            dlg_question.setStandardButtons(QMessageBox.StandardButton.Yes | QMessageBox.StandardButton.Cancel)
            dlg_question.setIcon(QMessageBox.Icon.Question)
            button = dlg_question.exec()

            if button == QMessageBox.StandardButton.Yes:
                os.makedirs(pathProjects)
            else:
                pass
            
        pathDistricts=standardizePath(dlg.lineEdit_pathDistricts.text())
        pathPostgresql=standardizePath(dlg.lineEdit_pathPostgresql.text())
        if not os.path.exists(pathDistricts):
            self.iface.messageBar().pushMessage("Info", "IDA Districts Path does not exist!", level=Qgis.Info)
        settings.setValue("pathDistricts", pathDistricts)
        settings.setValue("pathPostgresql", pathPostgresql)
        settings.setValue("districts_api_delay", dlg.lineEdit_districts_api_delay.text())
        settings.setValue("debug", dlg.checkBox_debug.checkState())
        settings.setValue("autosave", dlg.groupBox_autosave.isChecked())
        settings.setValue("autosave_dt", dlg.lineEdit_autosave_dt.text())
        settings.setValue("exportInvokedFeatures", dlg.checkBox_exportInvokedFeatures.checkState())
        settings.setValue("exportPrn", dlg.checkBox_exportPrn.checkState())
        settings.setValue("exportDbResults", dlg.checkBox_exportDbResults.checkState())
        settings.setValue("host", dlg.lineEdit_host.text())
        settings.setValue("port", dlg.lineEdit_port.text())
        try:
            #print(dlg.auth_dict)
            authId=dlg.auth_dict[dlg.comboBox_AuthId.currentText()]
            settings.setValue("auth_id", authId)
        except:
            authId=''
            settings.setValue("auth_id", authId)
        settings.endGroup()
        
        if dlg.checkBox_debug.isChecked() != self.config["debug"]:
            switchDebugMode(True if dlg.checkBox_debug.isChecked() else False,self.plugin_dir)
      
        #check if db settings settings changed
        if self.config["auth_id"]!=authId or self.config["host"]!=dlg.lineEdit_host.text() or self.config["port"]!=dlg.lineEdit_port.text():
            self.config= load_plugin_settings() #update config
            self.conn_postgres,self.cur_postgres,self.dlg.projectNames=connectDBPostgres(self.config,self.dlg)
            updateProjectNamesList(self.dlg,self.config)
        else:
            self.config= load_plugin_settings() #update config

        self.checkDBConnectionState()

        #timer for autosave
        if self.timer:
            self.timer.stop()
            if self.config['projectName']:
                self.timer.start(int(float(dlg.lineEdit_autosave_dt.text())*60*1000))
            dlg.close()
            
        closeDialog(dlg)
        self.dlg.statusMessage.setText(tr('@default','settings_saved'))

        #print("finished settings")
            
