    def switchTab(self,tab_name):
        try:
            # Find all tab widgets inside your dialog
            tab_widgets = self.dlg.findChildren(QTabWidget)

            if not tab_widgets:
                #print("No QTabWidget found")
                return

            tab_widget = tab_widgets[0]  # adjust if you have multiple

            # Loop through tabs and match by objectName
            for i in range(tab_widget.count()):
                widget = tab_widget.widget(i)
                if widget.objectName() == tab_name:
                    tab_widget.setCurrentIndex(i)
                    #print(f"Switched to tab: {tab_name}")
                    return

            #print(f"Tab not found: {tab_name}")
        except:
            pass

            
    def toggle_active_layer(self):
        layer = iface.activeLayer()
        if layer:
            node = QgsProject.instance().layerTreeRoot().findLayer(layer.id())
            if node:
                node.setItemVisibilityChecked(not node.isVisible())
            
